const escapeRegExp = (string) => {
  return string.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
};

export const emojify = (str) => Object.entries(lookup)
  .reduce(
    (s, [ascii, emoji]) => s.replace(new RegExp(`(?<!\\S)${escapeRegExp(ascii)}(?!\\S)`), emoji),
    str,
  );
